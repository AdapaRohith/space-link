export function seedDatabase() {
  if (localStorage.getItem('crm_seeded')) return false;

  localStorage.setItem('crm_users', JSON.stringify(DEFAULT_USERS));
  localStorage.setItem('crm_sources', JSON.stringify(DEFAULT_SOURCES));
  localStorage.setItem('crm_leads', JSON.stringify(SAMPLE_LEADS));
  localStorage.setItem('crm_visits', JSON.stringify(SAMPLE_VISITS));
  localStorage.setItem('crm_activities', JSON.stringify(SAMPLE_ACTIVITIES));
  localStorage.setItem('crm_seeded', 'true');
  return true;
}
